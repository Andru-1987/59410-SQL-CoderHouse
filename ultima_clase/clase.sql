-- FUNCION DE VENTANA CON CTES --> PRODUCTIVO

CREATE VIEW vw_analisis_estadistico AS (
WITH clean_data AS(
	SELECT 
		id,
		last_name,
		momento_pago,
		CAST(SUBSTR(COALESCE(pago,"$0.00"),2) AS DECIMAL(10,2)) AS pago
		FROM centro_medico.pagos
), ranking_datos AS (
SELECT 
	* ,
    -- funciones de ventana
    AVG(pago) OVER( PARTITION BY id, YEAR(momento_pago) ) AS promedio,
    RANK() OVER(PARTITION BY id, YEAR(momento_pago) ORDER BY momento_pago DESC) AS indice
FROM clean_data
ORDER BY id , momento_pago , pago
),
calculos_estadisticos AS(
SELECT 
	id,
	last_name,
    momento_pago,
    pago,
    promedio AS promedio_anual,
    (pago - LAG(pago,1,pago) OVER(PARTITION BY id ORDER BY momento_pago ))
    AS diff_year_anterior,
        CASE 
        WHEN LAG(pago, 1, pago) OVER (PARTITION BY id ORDER BY momento_pago) != 0 
        THEN 
        (pago - LAG(pago, 1, pago) OVER (PARTITION BY id ORDER BY momento_pago)) / 
             LAG(pago, 1, pago) OVER (PARTITION BY id ORDER BY momento_pago) * 100
        ELSE NULL
    END AS diff_percentage
FROM ranking_datos 
WHERE indice = 1 AND momento_pago IS NOT NULL
) SELECT
	id,
    last_name,
    momento_pago,
    ROUND(pago,2) AS pago,
    ROUND(promedio_anual,2) AS anual_avg,
    ROUND(diff_year_anterior,2) AS delta,
    CONCAT(ROUND(diff_percentage,2)," %" ) AS delta_perc
FROM calculos_estadisticos );


